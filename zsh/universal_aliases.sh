# these aliases work for both bash and zsh.
# append your ~/.bash_aliases or ~/.zsh_aliases file with them


# dev tools
## git
alias gcm="git commit -m"
alias gp="git pull"
alias main="git switch main"
## yarn
alias ys="yarn start"
alias yrd="yarn run dev"
alias yt="yarn test"
## tmux
alias ta="tmux attach -t"
alias tn="tmux new -s"
alias tk="tmux kill-session -t"

alias cat="bat -pp"
